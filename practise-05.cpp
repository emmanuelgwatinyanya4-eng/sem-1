#include<stdio.h>
int main()
{
	int month=12;
	switch(month)
	{
		case 1:
			printf("January");
			break;
			
			case 2:
				printf("February");
				break;
				
				case 3:
					printf("March");
					break;
					
					default:
						printf("NO EXECUTABLE CASES");
						break;
	}
	return 0;
}
